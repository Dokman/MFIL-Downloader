        public static IReadOnlyList<Repository> Repositories;

        static RepositoriesManager()
        {
            var mfils = Properties.Resources.mfils.Split(new[] { '\r', '\n' }, StringSplitOptions.RemoveEmptyEntries);

            Repositories = mfils.Select(x => new Repository(x)).Where(x => x.Valid).OrderBy(x => x.VersionName).ToList();
        }

        public static IReadOnlyList<Repository> GetByType(RepoType type)
        {
            return Repositories.Where(x => x.Type == type).ToList();
        }

        public static Repository GetRepositoryByMfil(string mfil)
        {
            return Repositories.FirstOrDefault(item => item.MfilName == mfil);
        }

